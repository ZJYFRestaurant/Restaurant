public class UsBean {
    private String shop_name;
    private String shop_tel;
    private String shop_address;
    private String shop_average_money;
    private String shop_time;

    public String getShop_name() {
        return shop_name;
    }

    public void setShop_name(String shop_name) {
        this.shop_name = shop_name;
    }

    public String getShop_tel() {
        return shop_tel;
    }

    public void setShop_tel(String shop_tel) {
        this.shop_tel = shop_tel;
    }

    public String getShop_address() {
        return shop_address;
    }

    public void setShop_address(String shop_address) {
        this.shop_address = shop_address;
    }

    public String getShop_average_money() {
        return shop_average_money;
    }

    public void setShop_average_money(String shop_average_money) {
        this.shop_average_money = shop_average_money;
    }

    public String getShop_time() {
        return shop_time;
    }

    public void setShop_time(String shop_time) {
        this.shop_time = shop_time;
    }
}
